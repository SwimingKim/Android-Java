
public class TestFor {
	public static void main(String[] args) {
		for (int i = 0; i < args.length; i++) {

		}
		// while (조건식) {
		// //조건식이 참이면 실행
		// }
		byte a = 7;

		while (a < 10) {
			System.out.println(a);
			a--;
		}

		do {
			// 조건식이 참이면 실행
			System.out.println("실행");
		} while (a < 7);

	}// end of main
}// end of class
